    private void jButtonExecutarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonExecutarActionPerformed

        Adjacencia ad = (Principal.escolhaMatriz ? Principal.matriz : Principal.lista);
        dijkstra.Dijkstra b = new dijkstra.Dijkstra(ad);

        int vertice = Integer.parseInt(jTextFieldVerticeInicial.getText());
        Vertice verticeInicial = new Vertice();
        verticeInicial.setPosicao(vertice);

        b.AlgoritmoDijsktra(ad, verticeInicial);

        DefaultTableModel modelo = (DefaultTableModel) jTableResultado.getModel();
        
        while(modelo.getRowCount() > 0){
            modelo.removeRow(0);
        }
        
        boolean max, orfao;
        Object[] o = new Object[4];
        String texto;
        for(Vertice v: b.resultados){
            max = orfao = false;
            Vertice fim = new Vertice();
            fim.setPosicao(v.getPosicao());
            if(v.getDistancia() == Integer.MAX_VALUE) max = true;
            if(v.getPI() == null) orfao = true;
            o[0] = v.getPosicao();
            o[1] = (orfao ? "Sem vértice pai" : v.getPI().getPosicao());
            o[2] = (max ? " - " : v.getDistancia());
            o[3] = b.printCaminho(verticeInicial, fim);
            modelo.addRow(o);
        }

    }//GEN-LAST:event_jButtonExecutarActionPerformed


