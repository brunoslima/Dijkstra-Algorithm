    private void jTextFieldVerticeInicialKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextFieldVerticeInicialKeyReleased

        if(jTextFieldVerticeInicial.getText().isEmpty()){
            jButtonExecutar.setEnabled(false);
            return;
        }

        int numero = -1;
        int tamanho = (Principal.escolhaMatriz ? Principal.matriz.getNumeroVertices() : Principal.lista.getNumeroVertices());

        try{
            numero = Integer.parseInt(jTextFieldVerticeInicial.getText());

        }catch(NumberFormatException e){
            jButtonExecutar.setEnabled(false);
        }

        if(numero >= 0 && numero < tamanho) jButtonExecutar.setEnabled(true);
        else jButtonExecutar.setEnabled(false);
    }//GEN-LAST:event_jTextFieldVerticeInicialKeyReleased

